public class Exemplo3 {

	public static void main(String[] args) {
	    int x,y;
		 x = 10 ;
		 System.out.println("Valor de X ao ser inicializado:" + x);
		 y=x++;
		 System.out.println("Valor de x++" + x);
		 System.out.println("Valor de y" + y);
		 
		 x = 10 ;
		 System.out.println("Valor de X ao ser inicializado:" + x);
		 y=++x;
		 System.out.println("Valor de ++x" + x);
		 System.out.println("Valor de y" + y);
		 
     
    /* ++ antes da variavewl= valor da variavel +1 na mesma linha
     * depois da variavel ++ = valor da variavel +1 na linha segunte*/
       	}

}
