package segundaaula;

public class Exemplo10 {

	public static void main(String[] args) {
		int i = 3;
		switch (i) {
		case 0:
			System.out.println("valor");
			break;
		case 1:
			System.out.println("valor 1");
			break;
		case 2:
			System.out.println("valor 2");
			break;
		case 3:
			System.out.println("valor 3");
			break;

		default:
			System.out.println("valor não encontrado");
			break;
		}

	}

}
